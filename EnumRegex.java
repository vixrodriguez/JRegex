/**
 * Enumerator that contains all the patterns of regular expressions that are used for JRegex
 * @author Víctor Rodríguez
 */
enum EnumRegex {
    INTEGER_PAT("^((-|\\+)?([0-9])+)$"),
    NUMERIC_PAT("^((-|\\+)?([0-9])+\\.[0-9]*)$"),
    PORT_NUMBER_PAT("(^\\d(\\d{0,4})?)$"),
    URL_PAT("^((http[s]?|ftp):\\/)?\\/?([^:\\/\\s]+)((\\/\\w+)*\\/)([\\w\\-\\.]+[^#?\\s]+)(.*)?(#[\\w\\-]+)?$"),
    EMAIL_PAT("^(([^<>()\\[\\]\\\\.,;:\\s@\"]+(\\.[^<>()\\[\\]\\\\.,;:\\s@\"]+)*)|(\".+\"))@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$"),
    HUMAN_NAME_PAT("^[a-zA-ZàáâäãåąčćęèéêëėįìíîïłńòóôöõøùúûüųūÿýżźñçčšžÀÁÂÄÃÅĄĆČĖĘÈÉÊËÌÍÎÏĮŁŃÒÓÔÖÕØÙÚÛÜŲŪŸÝŻŹÑßÇŒÆČŠŽ∂ð ,.\\'-]+$");
    
    private String regex; 
    
    private EnumRegex(String regex) {
        this.regex = regex;
    }

    public String getRegex() {
        return regex;
    }
}
